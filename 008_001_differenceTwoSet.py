"""
Write a Python Program to calculate difference of two set
A = {1,12,2,6,7,8} B = {15,0,1,3,6}
"""
a = {1, 12, 2, 6, 7, 8}
b = {15,0,1,3,6}
c = a.difference(b)
print(c)
print("\n")

"""
Write A Python Program To Calculate Symmetric Difference Of Two Set
A = {12,2,0,3,8} , B = {15,10,12,3,6}
"""
d = {12,2,0,3,8}
e = {15,10,12,3,6}
f = d.difference(e)

print(f)