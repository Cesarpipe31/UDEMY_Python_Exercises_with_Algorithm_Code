"""
    Write A Program That Performs All Arithmetic Operations On Two Variables.

"""

firstNumber = int(input("Enter first number: "))
secondNumber = int(input("Enter second number: "))

print("\n")
print("Addtion of two number : " + str(firstNumber + secondNumber))
print("Subtraction of two number : " + str(firstNumber - secondNumber))
print("Multiplication of two number : " + str(firstNumber*secondNumber))
print("Division of two number : " + str(firstNumber / secondNumber))
print("Module of two numbers : " + str(firstNumber%secondNumber))
